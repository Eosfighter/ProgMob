
public class Apartamento {

	private int numApart;
	private double valorCond;
	private int numBanheiros;
	private String endereco;
	private int numQuartos;
	private int vagasGaragem;
	private int andar;
	
	public int getNumApart() {
		return numApart;
	}
	public void setNumApart(int numApart) {
		this.numApart = numApart;
	}
	public double getValorCond() {
		return valorCond;
	}
	public void setValorCond(double valorCond) {
		this.valorCond = valorCond;
	}
	public int getNumBanheiros() {
		return numBanheiros;
	}
	public void setNumBanheiros(int numBanheiros) {
		this.numBanheiros = numBanheiros;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public int getNumQuartos() {
		return numQuartos;
	}
	public void setNumQuartos(int numQuartos) {
		this.numQuartos = numQuartos;
	}
	public int getVagasGaragem() {
		return vagasGaragem;
	}
	public void setVagasGaragem(int vagasGaragem) {
		this.vagasGaragem = vagasGaragem;
	}
	public int getAndar() {
		return andar;
	}
	public void setAndar(int andar) {
		this.andar = andar;
	}
	
	
	
}
